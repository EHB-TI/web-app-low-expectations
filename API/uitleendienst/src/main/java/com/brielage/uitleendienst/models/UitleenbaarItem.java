package com.brielage.uitleendienst.models;

import com.amazonaws.services.dynamodbv2.datamodeling.*;

import java.util.Date;

@DynamoDBTable(tableName = "uitleenbaarItem")
public class UitleenbaarItem {
    private String id;
    private String naam;
    private Integer eenheid;
    private Float prijs;
    private Date periode;
    private Categorie categorie;

    public UitleenbaarItem() {
    }

    public UitleenbaarItem(String naam, Integer eenheid, Float prijs, Date periode, Categorie categorie) {
        this.naam = naam;
        this.eenheid = eenheid;
        this.prijs = prijs;
        this.periode = periode;
        this.categorie = categorie;
    }

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    public String getId() {
        return id;
    }

    @DynamoDBAttribute
    public void setId(String id) {
        this.id = id;
    }

    @DynamoDBAttribute
    public String getNaam() {
        return naam;
    }

    @DynamoDBAttribute
    public void setNaam(String naam) {
        this.naam = naam;
    }

    @DynamoDBAttribute
    public Integer getEenheid() {
        return eenheid;
    }

    @DynamoDBAttribute
    public void setEenheid(Integer eenheid) {
        this.eenheid = eenheid;
    }

    @DynamoDBAttribute
    public Float getPrijs() {
        return prijs;
    }

    @DynamoDBAttribute
    public void setPrijs(Float prijs) {
        this.prijs = prijs;
    }

    @DynamoDBAttribute
    public Date getPeriode() {
        return periode;
    }

    @DynamoDBAttribute
    public void setPeriode(Date periode) {
        this.periode = periode;
    }

    @DynamoDBAttribute
    public Categorie getCategorie() {
        return categorie;
    }

    @DynamoDBAttribute
    public void setCategorie(Categorie categorie) {
        this.categorie = categorie;
    }
}
