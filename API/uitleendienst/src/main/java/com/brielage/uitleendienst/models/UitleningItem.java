package com.brielage.uitleendienst.models;

import com.amazonaws.services.dynamodbv2.datamodeling.*;

import java.util.Date;

@DynamoDBTable(tableName = "uitleningItem")
public class UitleningItem {
    private String id;
    private Uitlening uitlening;
    private UitleenbaarItem item;
    private Integer aantal;
    private Date teruggebrachtOp;
    private Integer aantalTeruggebracht;

    public UitleningItem() {
    }

    public UitleningItem(Uitlening uitlening, UitleenbaarItem item, Integer aantal, Date teruggebrachtOp, Integer aantalTeruggebracht) {
        this.uitlening = uitlening;
        this.item = item;
        this.aantal = aantal;
        this.teruggebrachtOp = teruggebrachtOp;
        this.aantalTeruggebracht = aantalTeruggebracht;
    }

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    public String getId() {
        return id;
    }

    @DynamoDBAttribute
    public void setId(String id) {
        this.id = id;
    }

    @DynamoDBAttribute
    public Uitlening getUitlening() {
        return uitlening;
    }

    @DynamoDBAttribute
    public void setUitlening(Uitlening uitlening) {
        this.uitlening = uitlening;
    }

    @DynamoDBAttribute
    public UitleenbaarItem getItem() {
        return item;
    }

    @DynamoDBAttribute
    public void setItem(UitleenbaarItem item) {
        this.item = item;
    }

    @DynamoDBAttribute
    public Integer getAantal() {
        return aantal;
    }

    @DynamoDBAttribute
    public void setAantal(Integer aantal) {
        this.aantal = aantal;
    }

    @DynamoDBAttribute
    public Date getTeruggebrachtOp() {
        return teruggebrachtOp;
    }

    @DynamoDBAttribute
    public void setTeruggebrachtOp(Date teruggebrachtOp) {
        this.teruggebrachtOp = teruggebrachtOp;
    }

    @DynamoDBAttribute
    public Integer getAantalTeruggebracht() {
        return aantalTeruggebracht;
    }

    @DynamoDBAttribute
    public void setAantalTeruggebracht(Integer aantalTeruggebracht) {
        this.aantalTeruggebracht = aantalTeruggebracht;
    }
}
